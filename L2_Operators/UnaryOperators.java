public class UnaryOperators {
    public static void main(String args[]) {
        // Pre-Increament
        int a = 10;
        int b = ++a;
        System.out.println("a = "+ a);
        System.out.println("b = "+ b);

        // Post-Increament
        int c = 10;
        int d = a++;
        System.out.println("c = "+ c);
        System.out.println("d = "+ d);
    }
}
